package com.example.fm13dt160calibration;

public class Parameter {
	private String uid;
	private double deviceTp;
	private String ucode;
	
	public String getUid() {
		return uid;
	}
	
	public void setUid(String uid) {
		this.uid=uid;
	}
	
	public double getDeviceTp() {
		return deviceTp;
	}
	
	public void setDeviceTp(double deviceTp) {
		this.deviceTp=deviceTp;
	}
	public String getUcode() {
		return ucode;
	}
	public void setUcode(String ucode) {
		this.ucode=ucode;
	}

}
